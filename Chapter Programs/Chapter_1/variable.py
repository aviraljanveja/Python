# Variable assignment
name = "Avi"

# Using variables
print("My name is " + name + ".")  # Output: My name is Avi.

# Examples of different data types
integer_variable = 42
float_variable = 3.14
string_variable = "Hello, Python!"
boolean_variable = True
my_variable = None

# Using the built-in type() function
print(type(integer_variable))  # Output: <class 'int'>
print(type(float_variable))  # Output: <class 'float'>
print(type(string_variable))  # Output: <class 'str'>
print(type(boolean_variable))  # Output: <class 'bool'>
print(type(my_variable))  # Output: <class 'NoneType'>
