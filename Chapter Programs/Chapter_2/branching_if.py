# Simple if statement

x = 5

# Here, the program will check the conditions
# one by one and execute the block associated
# with the first true condition.

if x > 5:  # Condition
    print("x is greater than 5")  # Code to be executed if the condition is true
elif x < 5:  # elif is short for "else if"
    print("x is less than 5")
else:  # If none of the previous conditions were true, then do this.
    print("x is equal to 5")

# Output : x is equal to 5.
