# Common Arithmetic Operators :

a = 5
b = 3

print(a + b)  # Addition, Output = 8
print(a - b)  # Subtraction, Output = 2
print(a * b)  # Multiplication, Output = 15
print(a / b)  # Float Division, Output = 1.6666666666666667
print(a // b)  # Integer Division, Output = 1
print(a % b)  # Remainder, 5 divided 3, Remainder Output = 2
print(a ** b)  # Exponent, 5 raised to power 3, Output = 125
