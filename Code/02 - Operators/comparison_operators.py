# Comparison Operators

a = 5
b = 2

print(5 == 2)  # Equal to, remember '=' is the assignment operator. Output = False
print(5 != 2)  # Not Equal to. Output = True
print(5 > 2)  # Greater than. Output = True
print(5 < 2)  # Less than. Output = False
print(5 >= 2)  # Greater than or equal. Output = True
print(5 <= 2)  # Less than or equal. Output = False

# Chained Comparison
# Python allows chained comparisons, a concise and readable way
# to perform multiple comparisons in a single expression.

x = 5
print(1 < x < 10)  # This is equivalent to : 1 < x and x < 10. Output = True
