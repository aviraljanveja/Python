# Print statement and its variations

# Basic Example
print("Hello World")  # Output = Hello World

# Customization : Output on the same line instead of newline
print("My name is", end = " ")
print("Avi")  # Output = My name is Avi

# Newline & Numbers
print(5+5)  # Output = 10
print()  # Newline
print(5*6)  # Output = 30
