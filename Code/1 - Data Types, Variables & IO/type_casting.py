# Type Casting : The process of converting one data type to another.

age = 30
gpa = 8.5

print(age)  # 30
print(gpa)  # 8.5


# Casting using in-built functions such as int(), float() or bool().
print(float(age))  # 30.0
print(int(gpa))  # 8

name1 = "Bro"
name2 = ""

print(bool(name1))  # True
print(bool(name2))  # False
