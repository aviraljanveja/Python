# String Example.

hi = "hello there"
name = "ana"

# Concatenation Operator
greeting = hi + " " + name
print(greeting)

# Star Operator
silly = hi + (" " + name) * 3
print(silly)

# Indexing
my_string = "Python"
print(my_string[0])  # In Python, all indexing starts at 0. Output : P

# Length
length = len(my_string)
print(length)  # Output : 6
