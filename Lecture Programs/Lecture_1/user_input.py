# Taking User Input.

text = input("Type anything : ")
# By default, python always treats user inputs as strings.
# You need to cast to the appropriate data type to handle them.
print(5 * text)  # If user input : 5, Output : 55555

num = int(input("Type any integer : "))  # Casting type to integer.
print(5 * num)  # If user input : 5, Output : 25
