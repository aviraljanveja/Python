# Print statement and its variations

# Basic Example
print("Hello World")  # Output = Hello World

# Customization : Output on the same line instead of newline
print("My", end = "")
print("Name is", end = "_")
print("Avi")  # Output = MyName is_Avi

# Newline & Numbers
print(5+5)  # Output = 10
print()  # Newline
print(5*6)  # Output = 30
