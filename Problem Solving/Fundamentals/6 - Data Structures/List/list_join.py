# join() function
# Combines a list of strings into a single string, using a specified separator.

words = ["Hello", "World", "Python"]
sentence = " ".join(words)
print(sentence)  # Output: "Hello World Python"

fruits = ['apple', 'banana', 'orange']
text = ",".join(fruits)
print(text)  # Output: "apple,banana,orange"

chars = ['p', 'y', 't', 'h', 'o', 'n']
word = "".join(chars)
print(word)  # Output: "python"
