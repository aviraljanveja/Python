# Sets : A set is an unordered collection of unique items.
# Sets are mutable and allow operations like union, intersection and difference
# which are useful for various problem-solving scenarios.

"""
Key Characteristics:
Unordered: No index or order.
Unique Elements: No duplicates allowed.
Mutable: Elements can be added or removed.
"""

# defining a set
my_set = {1, 2, 3, 4}

# Creating an empty set
empty_set = set()  # Note: {} creates an empty dictionary, not a set.

print(my_set)  # Output = {1, 2, 3, 4}
print(empty_set)  # Output = set()
