# Text Alignment
# In Python, a string of text can be aligned left, right and center.

# ljust(width, character) : This method returns a left aligned string of length width. default character is space.
print("HackerRank".ljust(20, "_"))  # Output = HackerRank__________


 # center(width, character) : This method returns a centered string of length width.
print("HackerRank".center(20, "_"))  # Output = _____HackerRank_____


# rjust(width, character) : This method returns a right aligned string of length width.
print("HackerRank".rjust(20, "_"))  # Output = __________HackerRank
