# A string in Python is a sequence of characters enclosed within single or double quotes.
# Strings are immutable, meaning they cannot be changed after creation.

# Indexing :
# Access individual characters using their index.
# Index starts from 0 for the first character.
# Negative indices access characters from the end of the string.

text = "python"

print(text[0])  # Output = p
print(text[-6])  # Output = p

print(text[5])  # Output = n
print(text[-1])  # Output = n
