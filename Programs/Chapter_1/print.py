# Here is the basic print command.

print("Hello World!")
# Output : Hello World!

# It is particularly useful for quickly checking
# the values of variables or printing messages to understand
# how your code is executing.

# You can also concatenate strings within the print statement
# using the + operator.

print("Hello" + " " + "World." + " " + "My name is Python.")
# Output : Hello World. My name is Python.
